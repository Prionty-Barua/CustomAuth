    <h3><a href="#">Dhaka Metropolitan Police (DMP):</a></h3>
      <P>Dhaka Metropolitan police (DMP) came into being on 1st February 1976. Mr. E.A Chowdhury served as its first Commissioner while Mr. A.S.M. Shahjahan (later on made IGP, Secretary and Advisor to the Caretaker Government) as the Deputy Commissioner (HQ) and they jointly worked hard to setup an effective and professional Metropolitan Police.</p>
      <p>DMP started its journey with 12 police stations and 6,000 forces in 1976. Now, the total number of police station is 49, and they are -- Adabor, Airport, Badda, Banani, Bangshal, Bhashantek, Cantonment, Chackbazar, Darussalam, Daskhinkhan, Demra, Dhanmondi, Gandaria, Gulshan, Hazaribag, Jatrabari, Kadamtoli, Kafrul, Kalabagan, Kamrangirchar, Khilgaon, Khilkhet, Kotwali, Lalbag, Mirpur Model, Mohammadpur, Motijheel, Mugda, New Market, Pallabi, Paltan Model, Ramna Model, Rampura, Rupnagar, Sabujbag, Shah ali, Shahbag, Sherebanglanagar, Shyampur, Sutrapur, Shahjahanpur, Tejgaon, Tejgaon I/A, Turag, Uttara Model, Uttarkhan, Uttara West, Vatara and Wari. At present, the total strength of DMP (all ranks and status) is more than 26000.</p>
      <p>DMP is using modern equipment like the latest data tracker, portable explosive vapor detector, long distance explosive detector, bomb suits, mobile vehicle x-rays, concealed weapon detector, binoculars, heavy duty luggage scanners, water cannons, archway gates, portable archway gates, road divider cones, signal lights, flash lights, wheel locks, APC, battery operated remote controlled road block, pedestal LED light with sign, battery operated portable pedestal LED light, battery operated portable LED warning device, prodders, net throwers, counterfeit currency identification mechanism, bomb disposal robot, illuminating check-post etc. Beat policing has been inaugurated in almost all 49 police stations.</p>
  </div>
    <h3><a href="#">Chittagong Metropolitan Police (CMP):</a></h3>
      <p>Chittagong Metropolitan Police, established in November 30, 1978, is one of the most important units of Bangladesh police. It has a jurisdiction on an area of 304,66 sq km and serves the people with 08 circles, 16 police stations -- Kotwali, Chandgaon, Panchlaish, Doublemooring, Pahartali, Bandar, Baijid bostami, Hali Shohor, Kornafuli, Potenga, Bakolia, Akborsha, Shodhorgat, EPZ, Chokbazar and Kulshi.</p>
      <p>It has also 30 police outposts, 6 police boxes, 2 crime divisions -- North and Port; headquarters division, 1 city special branch, 5 investigation centers including 4 naval and 1 police box and 2 traffic division.CMP has a glorious role in the liberation war of Bangladesh. In 1971, the then SP martyr Shamsul Hoque led his fellow freedom fighters from police force during the fight with the  Pakistani Army.</p>
